pub fn build_packet() !void {
    var backing_buffer: [2048]u8 = undefined;

    var fba = std.heap.FixedBufferAllocator.init(&backing_buffer);
    const allocator = fba.allocator();

    const pkt_data: []u8 = try allocator.alloc(u8, tcp_syn_raw.len);
    defer allocator.free(pkt_data);

    std.mem.copyForwards(u8, pkt_data, tcp_syn_raw[0..]);

    print("Original: ({}) {x}\n", .{ pkt_data.len, pkt_data });

    var wire_packet = WirePacket.init(0, 0, pkt_data, LinkLayerProtocols.ETHERNET);

    var packet: Packet.Packet = try Packet.Packet.create(allocator);

    try packet.from_wire_packet(&wire_packet);

    var tcp_layer = packet.get_layer_of_type(TCP.TCPLayer) orelse {
        return;
    };

    packet.print_layers_meta();

    const page_allocator = std.heap.page_allocator;

    print("{s}\n", .{tcp_layer.to_string(page_allocator)});

    _ = try packet.delete_layer(TCP.TCPLayer);

    packet.print_layers_meta();

    print("({}) {x}\n", .{ packet.aligned_buffer.len, packet.aligned_buffer });

    var ip_layer = packet.get_layer_of_type(IPv4Layer) orelse {
        return;
    };

    print("{s}\n", .{ip_layer.to_string(page_allocator)});
}

pub fn main() !void {
    var backing_buffer: [2048]u8 = undefined;

    var fba = std.heap.FixedBufferAllocator.init(&backing_buffer);
    const allocator = fba.allocator();

    _ = &allocator;

    const page_allocator = std.heap.page_allocator;

    _ = &page_allocator;

    const raw_data = RawData.RawData{ .mutable = try allocator.alloc(u8, udp_raw.len) };

    std.mem.copyForwards(u8, raw_data.mutable, udp_raw[0..udp_raw.len]);

    var packet = try Packet.Packet.create(allocator);

    try packet.from_raw(raw_data, LinkLayerProtocols.ETHERNET);
    _ = &packet;

    _ = packet.get_layer_of_type(EthLayer) orelse {
        print("eth layer not found.\n", .{});
        return;
    };

    var layer_owner = LayerOwner{ .allocator_owned = .{ .data = undefined, .allocator = page_allocator } };

    var layer = try packet.extract_layer(EthLayer, &layer_owner) orelse {
        print("eth layer not found during extraction.\n", .{});
        return;
    };

    print("{s}\n", .{layer.ethLayer.to_string(page_allocator)});

    packet.print_layers_meta();
}

